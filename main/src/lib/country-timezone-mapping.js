// Get timezone for a country
export function getTimezoneForCountry(countryCode, region = null) {
  if (!countryCode) return 'UTC';
  
  // Handle special cases with regions
  if (countryTimezoneRegions[countryCode] && region) {
    return countryTimezoneRegions[countryCode][region] || countryTimezoneMap[countryCode] || 'UTC';
  }
  
  // Return default timezone for country
  return countryTimezoneMap[countryCode] || countryTimezoneMap['DEFAULT'];
}

